#ifndef SDF_CORE_TYPES_INCLUDED
#define SDF_CORE_TYPES_INCLUDED

#include <vector>
#include <sdf/core/config.hpp>
#include <sdf/core/math.hpp>

namespace sdf
{
	//----------------------------------------------------------------------------------------------------------------------
	/// @brief The index type used for index buffers
	//----------------------------------------------------------------------------------------------------------------------
	typedef unsigned int index_type;
	//----------------------------------------------------------------------------------------------------------------------
	/// @brief The vertex type - just a point (we dont use normals or anything yet)
	//----------------------------------------------------------------------------------------------------------------------
	typedef point3d vertex_type;
	//----------------------------------------------------------------------------------------------------------------------
	/// @brief A vertex array/vertex buffer
	//----------------------------------------------------------------------------------------------------------------------
	typedef std::vector<vertex_type> vertex_array;
	//----------------------------------------------------------------------------------------------------------------------
	/// @brief The index array/index buffer
	//----------------------------------------------------------------------------------------------------------------------
	typedef std::vector<index_type> index_array;
	//----------------------------------------------------------------------------------------------------------------------
	/// @brief The normal array/buffer - used mainly for the geometrical normals (not user defined)
	//----------------------------------------------------------------------------------------------------------------------
	typedef std::vector<vector3d> normal_array;
}

#endif // SDF_CORE_TYPES_INCLUDED