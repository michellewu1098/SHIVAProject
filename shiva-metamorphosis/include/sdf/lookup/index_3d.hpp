#ifndef SDF_LOOKUP_INDEX_3D_INCLUDED
#define SDF_LOOKUP_INDEX_3D_INCLUDED

namespace sdf
{
	namespace detail
	{
		//----------------------------------------------------------------------------------------------------------------------
		/// @class index_3d "include/sdf/lookup/index_3d.hpp"
		/// @brief Index in a 3d grid.
		/// @author Mathieu Sanchez
		/// @version 1.0
		/// @date Last Revision 28/06/11 Initial revision
		//----------------------------------------------------------------------------------------------------------------------
		class index_3d
		{
		public :
			//----------------------------------------------------------------------------------------------------------------------
			/// @brief Default ctor
			//----------------------------------------------------------------------------------------------------------------------
			index_3d();
			//----------------------------------------------------------------------------------------------------------------------
			/// @brief Ctor
			/// @param[in] i_x Index in dimension X
			/// @param[in] i_y Index in dimension Y
			/// @param[in] i_z Index in dimension Z
			//----------------------------------------------------------------------------------------------------------------------
			index_3d(int i_x,int i_y,int i_z);
			//----------------------------------------------------------------------------------------------------------------------
			/// @brief Set index 
			/// @param[in] i_x Index in dimension X
			/// @param[in] i_y Index in dimension Y
			/// @param[in] i_z Index in dimension Z
			//----------------------------------------------------------------------------------------------------------------------
			void set(int i_x,int i_y,int i_z);

			//----------------------------------------------------------------------------------------------------------------------
			/// @brief Get index in dimension X
			/// @return Index in X
			//----------------------------------------------------------------------------------------------------------------------
			const int& x() const;
			//----------------------------------------------------------------------------------------------------------------------
			/// @brief Get index in dimension Y
			/// @return Index in Y
			//----------------------------------------------------------------------------------------------------------------------
			const int& y() const;
			//----------------------------------------------------------------------------------------------------------------------
			/// @brief Get index in dimension Z
			/// @return Index in Z
			//----------------------------------------------------------------------------------------------------------------------
			const int& z() const;

			//----------------------------------------------------------------------------------------------------------------------
			/// @brief Get index in a dimension
			/// @param[in] i The dimension 0 is X, 1 is Y, 2 is Z
			/// @return Index in dimension (X|Y|Z)
			//----------------------------------------------------------------------------------------------------------------------
			const int& operator[](int i) const;
		private :
			//----------------------------------------------------------------------------------------------------------------------
			/// @brief Coordinates in each dimension
			//----------------------------------------------------------------------------------------------------------------------
			int m_coord[3];
		};
	}
}

#endif /* SDF_LOOKUP_INDEX_3D_INCLUDED */
