#include <sdf/lookup/index_3d.hpp>
#include <assert.h>

//----------------------------------------------------------------------------------------------------------------------
sdf::detail::index_3d::index_3d()
{
	set(0,0,0);
}

//----------------------------------------------------------------------------------------------------------------------
sdf::detail::index_3d::index_3d(int i_x,int i_y,int i_z)
{
	set(i_x,i_y,i_z);
}

//----------------------------------------------------------------------------------------------------------------------
void sdf::detail::index_3d::set(int i_x,int i_y,int i_z)
{
	m_coord[0] = i_x;
	m_coord[1] = i_y;
	m_coord[2] = i_z;
}


//----------------------------------------------------------------------------------------------------------------------
const int& sdf::detail::index_3d::x() const { return m_coord[0]; }
//----------------------------------------------------------------------------------------------------------------------
const int& sdf::detail::index_3d::y() const { return m_coord[1]; }
//----------------------------------------------------------------------------------------------------------------------
const int& sdf::detail::index_3d::z() const { return m_coord[2]; }

//----------------------------------------------------------------------------------------------------------------------
const int& sdf::detail::index_3d::operator[](int i) const
{
	assert(i<3);
	return m_coord[i];
}