namespace ShivaGUI
{

	/// This is a 'model' for a Model-View-Adapter pattern that is designed to work with a DataAdapter and a View of some sort
	class DataProvider
	{
	public:

		/// Tests whether an attribute exists for an entry
		virtual bool QueryAttribute(int dataEntry, std::string attribute) = 0;

		/// Retrieves an attribute from an entry
		virtual std::string GetAttributeString(int dataEntry, std::string attribute) = 0;

		/// Retrieves the number of entries in this Provider
		virtual int GetNumEntries() = 0;

	protected:

	};

}

