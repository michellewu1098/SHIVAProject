///-----------------------------------------------------------------------------------------------

#ifndef __SHIVA_GUISYSTEM_LINEARLAYOUTPARAMS__
#define __SHIVA_GUISYSTEM_LINEARLAYOUTPARAMS__

#include "GUI/Views/ViewGroups/LayoutParams.h"


namespace ShivaGUI
{
	class LinearLayoutParams : public LayoutParams
	{
	public:

		//----------------------------------------------------------------------------------
		/// \brief Default ctor
		//----------------------------------------------------------------------------------
		LinearLayoutParams();
		//----------------------------------------------------------------------------------
		/// \brief Ctor
		/// \param [in] _weight
		//----------------------------------------------------------------------------------
		LinearLayoutParams( float _weight );
		//----------------------------------------------------------------------------------
		/// \brief Set weight
		/// \param [in] _value
		//----------------------------------------------------------------------------------
		void SetWeight( const float &_value ) { m_weight = _value; }
		//----------------------------------------------------------------------------------
		/// \brief Returns weight
		/// \return m_weight
		//----------------------------------------------------------------------------------
		float GetWeight() const { return m_weight; }
		//----------------------------------------------------------------------------------

	protected:

		//----------------------------------------------------------------------------------
		/// \brief Weight
		//----------------------------------------------------------------------------------
		float m_weight;
		//----------------------------------------------------------------------------------

	};
}

#endif
