#ifndef GLVOLUMETEXTURE_H
#define GLVOLUMETEXTURE_H

#include <GL/GLee.h>
#include <GL/glu.h>
#include <sdf/discretization/grid.hpp>

class GLVolumeTexture
{
public :
	//----------------------------------------------------------------------------------------------------------------------
	/// @brief Constructor
	//----------------------------------------------------------------------------------------------------------------------
        GLVolumeTexture();
        bool update(const sdf::grid& i_volume);
	//----------------------------------------------------------------------------------------------------------------------
	/// @brief Bind the volume texture to openGL
	//----------------------------------------------------------------------------------------------------------------------
	void bind();
	//----------------------------------------------------------------------------------------------------------------------
	/// @brief Unbind the texture (sets 0 instead)
	//----------------------------------------------------------------------------------------------------------------------
	void unbind();

	//----------------------------------------------------------------------------------------------------------------------
	/// @brief Validity of the texture
	/// @return True if it seems valid, false otherwise
	//----------------------------------------------------------------------------------------------------------------------
	bool valid();

	//----------------------------------------------------------------------------------------------------------------------
	/// @brief Destructor
	//----------------------------------------------------------------------------------------------------------------------
	~GLVolumeTexture();
private :
	GLuint m_texId;
	unsigned int m_width,m_height,m_depth;
};

#endif // GLVOLUMETEXTURE_H
